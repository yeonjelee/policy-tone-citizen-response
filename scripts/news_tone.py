"""
보도 논조(부정성) 사전 + 점수기  [2026-06-16]

H2의 프레이밍 변수 = '정책 보도의 부정적 논조 강도'(공격적↔우호적). Lamla&Lein tone channel.
경제뉴스 제목에 특화한 부정/긍정 어휘 사전(BoW). KNU 일반 감성사전으로 교체 가능.

negativity = (neg − pos) / (neg + pos) ∈ [-1, +1]   (+1=매우 공격/부정, −1=우호/긍정)

주의: 제목 사전 기반이라 '정책 비판'과 '나쁜 소식'을 완벽히 가르진 못함 →
      '보도 부정 논조 강도'로 해석(정책-directed 아님).
"""
import re

NEG = [
    "우려", "충격", "급등", "폭등", "치솟", "불안", "위기", "위험", "부담", "악화",
    "비판", "논란", "후퇴", "둔화", "침체", "경고", "리스크", "타격", "직격탄", "폭탄",
    "쇼크", "한파", "빨간불", "비상", "패닉", "추락", "곤두박질", "압박", "먹구름",
    "절벽", "공포", "혼란", "갈등", "반발", "실패", "부실", "디폴트", "경색", "위축",
    "줄도산", "줄폐업", "고통", "한숨", "눈물", "직격", "줄인상", "줄줄이", "최악",
    "하락세", "마이너스", "역대 최저", "악재", "흔들", "휘청", "비관", "암울", "냉각",
]
POS = [
    "개선", "회복", "호조", "안정", "반등", "기대", "호황", "훈풍", "청신호", "완화",
    "진정", "활기", "도약", "선방", "호전", "상승세", "순항", "기지개", "온기", "낙관",
    "호평", "활성화", "성과", "역대 최고", "플러스", "회복세", "최대 실적", "수혜",
    "반색", "화색", "탄력", "개선세", "好",
]


def score_negativity(text: str):
    """반환: (neg, pos, negativity[-1~1])"""
    t = re.sub(r"\s+", " ", str(text))
    n = sum(t.count(w) for w in NEG)
    p = sum(t.count(w) for w in POS)
    neg = (n - p) / (n + p) if (n + p) > 0 else None
    return n, p, neg


if __name__ == "__main__":
    for s in ["물가 급등에 서민 부담 가중…경기 침체 우려", "수출 회복 기대…경기 호조 청신호", "기준금리 동결"]:
        print(f"{score_negativity(s)}  | {s}")

"""
재정 어조(tone_fiscal) 커스텀 방향사전 + 점수기  [2026-06-16]

eKoNLPy 는 통화정책 사전이라 재정 어휘가 없어 fiscal 을 못 매긴다(추경 문서조차 표현 1개).
→ 기재부(MOEF) 보도자료 본문에 맞춘 자체 사전. 부호 = METHODOLOGY 통일 컨벤션:
   +1 = 재정 긴축/건전화(보수), −1 = 확장(부양).

설계: 표면형 구문 빈도(부분문자열). 다어절 구문이라 부정 오매칭 적음.
점수: tone_fiscal = (P − N) / (P + N),  P=긴축어 빈도, N=확장어 빈도.

주의: 시드 사전이다. 전체 재수집 MOEF 본문에서 누락 어휘를 보강할 것(근거 기반 확장).
"""

import re

# +1 : 재정 건전화 / 긴축 / 보수적
TIGHTEN = [
    "재정건전성", "재정 건전성", "건전재정", "재정건전화", "건전화",
    "지출 구조조정", "지출구조조정", "지출 효율화", "지출효율화", "지출 절감", "지출절감",
    "재량지출 축소", "지출 재구조화", "세출 구조조정", "긴축", "긴축재정",
    "재정준칙", "국가채무 관리", "채무 감축", "채무 축소", "적자 축소", "적자 감축",
    "세입 확충", "세원 확충", "재정 정상화", "재정정상화", "허리띠", "씀씀이",
    "알뜰", "재정개혁", "강도 높은 지출", "재정여력 확보", "건전성 관리",
]
# −1 : 재정 확장 / 적극 / 부양
EXPAND = [
    "확장재정", "확장적 재정", "확장적인 재정", "적극재정", "적극적 재정", "적극적인 재정",
    "추가경정예산", "추가경정", "추경", "재정 투입", "재정투입", "재정 보강",
    "지출 확대", "지출확대", "재정지출 확대", "재정 투자 확대", "투자 확대",
    "조기집행", "조기 집행", "재정 조기", "신속집행", "경기 대응", "경기대응",
    "부양", "확대 편성", "적극적 역할", "재정의 적극", "확장적", "재정 조기집행",
    "민생 지원", "재정 역할 강화", "위기 대응",
]


def score_fiscal(text: str):
    """반환: (P_긴축, N_확장, polarity[-1~1], hits)"""
    t = re.sub(r"\s+", " ", text)
    P = sum(t.count(w) for w in TIGHTEN)
    N = sum(t.count(w) for w in EXPAND)
    pol = (P - N) / (P + N) if (P + N) > 0 else None
    return P, N, pol


if __name__ == "__main__":
    # 간단 검증
    ex_expand = "정부는 경기 대응을 위해 추가경정예산을 편성하고 재정지출 확대와 조기집행을 추진한다."
    ex_tight = "재정건전성 확보를 위해 지출 구조조정과 지출 효율화로 국가채무 관리를 강화하고 재정준칙을 도입한다."
    for name, s in [("확장 예시", ex_expand), ("건전화 예시", ex_tight)]:
        P, N, pol = score_fiscal(s)
        print(f"{name}: P(긴축)={P} N(확장)={N} tone_fiscal={pol:+.2f}")

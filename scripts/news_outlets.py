_LOOKUP = {name: lean for lean, names in LEAN_MAP.items() for name in names}


def classify_lean(press: str) -> str:
    if not press:
        return "etc"
    p = str(press).strip()
    if p in _LOOKUP:
        return _LOOKUP[p]
    for name, lean in _LOOKUP.items():
        if name in p:
            return lean
    return "etc"


